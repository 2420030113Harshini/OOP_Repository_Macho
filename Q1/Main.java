/* Q1: Write a Java program to declare variables of all primitive data types. Initialize them with valid values and print their sizes using appropriate wrappers.
Ans) JAVA CODE: */

public class Main {
  public static void main(String[] args) {
    byte myNum = 100;
    System.out.println(myNum);  
    int a = 25;
    System.out.println(a);
    long d = 323232L;
    System.out.println(d);
    short t = 3434;
    System.out.println(t);
    float y = 899f;
    System.out.println(y);
    double r = 789.25d;
    System.out.println(r);
    boolean isgourdTasty = false;
    System.out.println(isgourdTasty);
    String name = "Johnson";
    System.out.println(name);
    char f = 'e';
    System.out.println(f);
  }
}

